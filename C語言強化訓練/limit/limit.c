#include <stdio.h>
#include <limits.h>

int main(){
    printf("%d\n",SCHAR_MIN);//signed char
    printf("%d\n",SCHAR_MAX);//signed char

    printf("%d\n",INT_MAX);//signed int
    printf("%d\n",INT_MAX);//signed int

    printf("%u\n",UINT_MAX);//unsigned int

    printf("%d\n",0x10);
    printf("%d\n",010);
    return 0;
}